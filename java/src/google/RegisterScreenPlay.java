package google;

public class RegisterScreenPlay {
	
	private Long idMovie;
	private Long idScreenPlay;
	
	public RegisterScreenPlay(Long idMovie, Long idScreenPlay) {
		this.idMovie = idMovie;
		this.idScreenPlay = idScreenPlay;
	}

	public Long getIdMovie() {
		return idMovie;
	}

	public Long getIdScreenPlay() {
		return idScreenPlay;
	}
	
	

}
